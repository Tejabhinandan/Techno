var bike=["Rx100","R15","ZMR","Duke"]
// foreach
bike.forEach(val => {
    return console.log(`bike:${val}`)    
});
console.log("======================================");

// push
var a=bike.push("RC 200")
console.log("push :",a)
console.log("======================================");

// pop
var a1=bike.pop()
console.log("pop :",a1)
console.log("======================================");

// unshift
var a2=bike.unshift("duke 125")
console.log("unshift :",a2)
console.log("======================================");

// shift
var a3=bike.shift()
console.log("shift :",a3)
console.log("======================================");

// includes
var a4=bike.includes("R15");
console.log("includes :",a4)
console.log("======================================");

// splice
var a5=bike.splice(3,1,"tiger")
console.log("splice :",a5)
console.log("======================================");

// slice
var a6=bike.slice(0,3)
console.log("slice :",a6)
console.log("======================================");
// isArray
var a7=Array.isArray(bike)
console.log("isArray :",a7)
console.log("======================================");


